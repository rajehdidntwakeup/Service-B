package test.serviceb.domain;

import jakarta.persistence.Entity;
import jakarta.persistence.GeneratedValue;
import jakarta.persistence.GenerationType;
import jakarta.persistence.Id;
import jakarta.persistence.JoinColumn;
import jakarta.persistence.ManyToOne;

/**
 * Represents an item entity within an order.
 * This class defines the structure and behavior of an order item and includes
 * details such as the item's identifier, name, quantity, price, and the associated order.
 * It is annotated with JPA annotations to facilitate database persistence.
 */
@Entity
public class OrderItem {

  /**
   * The unique identifier for the "Item" entity.
   * This field is autogenerated using the IDENTITY strategy.
   */
  @Id
  @GeneratedValue(strategy = GenerationType.IDENTITY)
  private int id;

  /**
   * Represents the unique identifier of an item.
   * This field maps to the "item" entity and serves as a reference
   * to identify a specific item within the context of an order.
   */
  private int itemId;


  /**
   * Represents the name of the item in the context of an order.
   * This field stores a human-readable, descriptive name for the item, which
   * can be used for display purposes or in user-facing operations.
   */
  private String itemName;

  /**
   * Represents the quantity of a specific item in an order.
   * This field stores the numerical value denoting the count of items
   * included for a particular {@link OrderItem} instance.
   */
  private int quantity;

  /**
   * Represents the price of an individual item in an order.
   * This field stores the cost associated with a single unit of the item.
   * The value is stored as a double to account for fractional currency values.
   */
  private double price;

  /**
   * Represents the order associated with a specific {@link OrderItem}.
   * This field establishes a many-to-one relationship between {@link OrderItem} and {@link Orders}.
   * It is mapped to the foreign key column "orderId" in the database and cannot be null.
   */
  @ManyToOne
  @JoinColumn(name = "orderId", nullable = false)
  private Orders order;

  /**
   * Default constructor for the OrderItem class.
   * Initializes an instance of the OrderItem entity with default values for all fields.
   */
  public OrderItem() {
  }

  /**
   * Constructs an instance of the OrderItem class with specified details.
   *
   * @param itemId   the unique identifier for the item
   * @param itemName the name of the item
   * @param quantity the quantity of the item in the order
   * @param price    the price of an individual item
   */
  public OrderItem(int itemId, String itemName, int quantity, double price) {
    this.itemId = itemId;
    this.itemName = itemName;
    this.quantity = quantity;
    this.price = price;
  }

  /**
   * Retrieves the unique identifier of this order item.
   *
   * @return the unique identifier as an integer
   */
  public int getId() {
    return id;
  }

  /**
   * Retrieves the unique identifier associated with the item.
   *
   * @return the unique identifier of the item as an integer
   */
  public int getItemId() {
    return itemId;
  }

  /**
   * Sets the identifier for the item associated with this order item.
   *
   * @param itemId the identifier of the item to be set
   */
  public void setItemId(int itemId) {
    this.itemId = itemId;
  }


  /**
   * Retrieves the name of the item associated with this order item.
   *
   * @return the name of the item as a String
   */
  public String getItemName() {
    return itemName;
  }

  /**
   * Updates the name of the item associated with this order item.
   *
   * @param itemName the new name of the item to be set
   */
  public void setItemName(String itemName) {
    this.itemName = itemName;
  }

  /**
   * Retrieves the quantity of the order item.
   *
   * @return the quantity as an integer
   */
  public int getQuantity() {
    return quantity;
  }

  /**
   * Updates the quantity of the order item.
   *
   * @param quantity the new quantity to set for this order item
   */
  public void setQuantity(int quantity) {
    this.quantity = quantity;
  }

  /**
   * Retrieves the price of the order item.
   *
   * @return the price of the order item as a double
   */
  public double getPrice() {
    return price;
  }

  /**
   * Sets the price for this order item.
   *
   * @param price the price to set for the order item
   */
  public void setPrice(double price) {
    this.price = price;
  }

  /**
   * Retrieves the order associated with this order item.
   *
   * @return the associated {@link Orders} instance
   */
  public Orders getOrder() {
    return order;
  }

  /**
   * Sets the order associated with this order item.
   *
   * @param order the {@link Orders} instance to be associated with this order item
   */
  public void setOrder(Orders order) {
    this.order = order;
  }
}
